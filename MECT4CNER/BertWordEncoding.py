import numpy as np
import torch
import transformers

# 加载预训练的中文BERT模型
tokenizer = transformers.BertTokenizer.from_pretrained("bert-base-chinese")
model = transformers.BertModel.from_pretrained("bert-base-chinese")


def word_embedding(vec_list, word):
    """
    获取句子中某个单词的词向量
    :param vec_list: 句子中每个汉字的向量列表，一个包含n个向量的列表
    :param word: 单词字符串
    :return: 词向量，一个numpy数组
    """
    # 将向量列表转换为BERT模型需要的格式
    tokens = [tokenizer.convert_tokens_to_ids(tokenizer.cls_token)] + [tokenizer.convert_tokens_to_ids(word) for word in word.split()] + [tokenizer.convert_tokens_to_ids(tokenizer.sep_token)]
    input_ids = torch.tensor([tokens])
    vec_list = torch.tensor(vec_list)
    attention_mask = torch.ones_like(input_ids)

    # 将输入传入BERT模型
    with torch.no_grad():
        outputs = model(input_ids, attention_mask=attention_mask, token_type_ids=None)
    last_hidden_state = outputs.last_hidden_state

    # 池化输出，得到单词的词向量
    word_vec = np.mean(last_hidden_state.detach().numpy()[0][1:-1], axis=0)
    return word_vec


